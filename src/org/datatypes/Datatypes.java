package org.datatypes;

public class Datatypes {
	public void data() {
		int a=10;
		int b=20;
		int c=a+b;
		System.out.println(c);
		byte a1=22;
		byte b1=23;
		int c1=b1+a1;
		System.out.println("the difference is "+c1);
		short s=235;
		short d=22;
		int s1=s*d;
		System.out.println("the united value is "+s1);
		long l=964545454l;
		System.out.println("the long value is "+l);
		float fa=0213.23564f;
		System.out.println("the float value is "+fa);
		char cc='$';
		System.out.println("the charecter is "+cc);
		String ss="i am doing math";
		System.out.println("the string is "+ss);
		
	}
	public static void main(String[] args) {
		Datatypes x=new Datatypes();
		x.data();
	}

}
